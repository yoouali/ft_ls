#include "libft.h"

int		ft_gword(char const *s, char c)
{
	int i;
	int len;

	i = 0;
	len = 0;
	while (*s)
	{
		if (*s == c)
			i = 0;
		else if (++i == 1)
			len++;
		s++;
	}
	return (len);
}
